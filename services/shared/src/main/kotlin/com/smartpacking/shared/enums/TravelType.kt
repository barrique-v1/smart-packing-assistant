package com.smartpacking.shared.enums

/**
 * Types of travel that affect packing recommendations.
 */
enum class TravelType {
    /**
     * Business travel - formal attire, work equipment.
     */
    BUSINESS,

    /**
     * Leisure vacation - casual attire, relaxation items.
     */
    VACATION,

    /**
     * Backpacking/adventure travel - minimal gear, outdoor equipment.
     */
    BACKPACKING
}
